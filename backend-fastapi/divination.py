def 원국_형충공망(지지목록, 공망지지) -> list:
    """4지지[년월일시] + 공망지지(set) → [(조건명, wonmyung_idx)]. wonmyung 대10/17/18/19/23/24."""
    s = set(지지목록)
    found = []
    # 충(대10)
    for i, (a, b) in enumerate(_충, 1):
        if a in s and b in s:
            found.append((f"{a}{b} 충(沖)", f"10-{i}"))
    # 육합(대24)
    for i, (a, b) in enumerate(_육합, 1):
        if a in s and b in s:
            found.append((f"{a}{b} 육합(六合)", f"24-{i}-A"))
    # 방합(대23)
    for i, trio in enumerate(_방합, 1):
        if all(x in s for x in trio):
            found.append((f"{''.join(trio)} 방합(方合)", f"23-{i}"))
    # 자형(대18)
    for i, z in enumerate(_자형, 1):
        if 지지목록.count(z) >= 2:
            found.append((f"{z}{z} 자형(自刑)", f"18-{i}-A"))
    # 삼형(대17)
    if {"寅", "巳", "申"} <= s:
        found.append(("寅巳申 삼형(三刑)", "17-1"))
    if {"丑", "戌", "未"} <= s:
        found.append(("丑戌未 삼형(三刑)", "17-5"))
    if {"子", "卯"} <= s:
        found.append(("子卯 형(刑)", "17-9"))
    # 공망(대19): 년/월/일/시지가 공망지지에 들면
    위치 = ["년지", "월지", "일지", "시지"]
    gm = []
    for idx, z in enumerate(지지목록):
        if z in 공망지지:
            gm.append(idx)
            found.append((f"{위치[idx]} 공망(空亡)", f"19-{idx + 1}"))
    return found


