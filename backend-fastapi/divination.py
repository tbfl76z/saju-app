# 8괘 자연상징(한글) → 3비트(아래효=bit0, 양=1). 작괘_괘상과 동일 규칙
# 천☰111 태☱011 리☲101 진☳001 손☴110 감☵010 간☶100 곤☷000
TRIGRAM_BIN = {"천": 0b111, "택": 0b011, "화": 0b101, "뢰": 0b001,
               "풍": 0b110, "수": 0b010, "산": 0b100, "지": 0b000}
BIN_TRIGRAM = {v: k for k, v in TRIGRAM_BIN.items()}


def 동전_작괘():
    """6효 동전점. 각 효: 동전3개 합(양면수). 6=노음(변),7=소양,8=소음,9=노양(변).
    아래(1효)→위(6효). 본괘 + 변효 반환."""
    yos = []
    for _ in range(6):
        coins = sum(_random.randint(0, 1) for _ in range(3))  # 양면 개수 0~3
        val = 6 + coins  # 6~9
        yos.append(val)
    return yos  # [효1..효6]


def 작괘_괘상(yos):
    """6효 → (상괘bin, 하괘bin, 본괘 음양효, 변효위치)."""
    yin_yang = [y % 2 for y in yos]  # 7,9→1(양) / 6,8→0(음)
    하괘 = yin_yang[0] | (yin_yang[1] << 1) | (yin_yang[2] << 2)
    상괘 = yin_yang[3] | (yin_yang[4] << 1) | (yin_yang[5] << 2)
    변효 = [i + 1 for i, y in enumerate(yos) if y in (6, 9)]
    return 상괘, 하괘, yin_yang, 변효


