NAPEUM = {}
for pair, oh in _납음:
    for i in range(0, len(pair), 2):
        NAPEUM[pair[i:i + 2]] = oh

# 五行局 명칭/숫자
OHENG_GUK = {"水": ("水二局", 2), "木": ("木三局", 3), "金": ("金四局", 4),
             "土": ("土五局", 5), "火": ("火六局", 6)}

# 五虎遁: 年干 → 寅월의 천간 인덱스 (甲己→丙, 乙庚→戊, 丙辛→庚, 丁壬→壬, 戊癸→甲)
_오호둔 = {0: 2, 5: 2, 1: 4, 6: 4, 2: 6, 7: 6, 3: 8, 8: 8, 4: 0, 9: 0}


def 명궁_지지(음력월: int, 시지_idx: int) -> int:
    """명궁 지지 인덱스. 寅(=2)에서 정월 시작 順行 (음력월-1)칸 → 생월궁,
    거기서 생시(子0..亥11) 만큼 逆行. (캡처 2/2 검증)"""
    pos = (2 + (음력월 - 1)) % 12
    pos = (pos - 시지_idx) % 12
    return pos


def 명궁_천간(년간_idx: int, 명궁지지_idx: int) -> int:
    """五虎遁으로 명궁 지지의 천간 인덱스."""
    인월천간 = _오호둔[년간_idx]
    # 寅(idx2)부터 順行하며 천간도 1씩 증가
    걸음 = (명궁지지_idx - 2) % 12
    return (인월천간 + 걸음) % 10


def 자미_오행국(년간_idx: int, 음력월: int, 시지_idx: int) -> dict:
    """자미두수 五行局 + 명궁 산출."""
    mg = 명궁_지지(음력월, 시지_idx)
    cg = 명궁_천간(년간_idx, mg)
    ganzhi = 천간[cg] + 지지[mg]
    oh = NAPEUM[ganzhi]
    guk_name, guk_no = OHENG_GUK[oh]
    return {"명궁": 지지[mg], "명궁간지": ganzhi, "납음오행": oh,
            "五行局": guk_name, "국수": guk_no}


# --- 자미두수 자미 안성 + 14주성 (原局 캡처로 검증) ----------------
def 자미_위치(국수: int, 음력생일: int) -> int:
    """자미성 지지 인덱스. 보수법. (金4국11일→卯3 검증)"""
    보수 = (국수 - 음력생일 % 국수) % 국수
    商 = (음력생일 + 보수) // 국수
    val = 商 + 보수 if 보수 % 2 == 0 else 商 - 보수
    return (2 + (val - 1)) % 12  # 寅(2) 기준


# 자미계 6성(자미 기준 역행 offset), 천부계 8성(천부 기준 순행 offset)
_자미계 = {"자미": 0, "천기": -1, "태양": -3, "무곡": -4, "천동": -5, "염정": -8}
_천부계 = {"천부": 0, "태음": 1, "탐랑": 2, "거문": 3, "천상": 4, "천량": 5, "칠살": 6, "파군": 10}


def 자미_14주성(국수: int, 음력생일: int) -> dict:
    """14주성 지지배치 → {지지idx: [주성...]}. 천부 = (4-자미)%12."""
    Z = 자미_위치(국수, 음력생일)
    F = (4 - Z) % 12
    chart = {i: [] for i in range(12)}
    for star, off in _자미계.items():
        chart[(Z + off) % 12].append(star)
    for star, off in _천부계.items():
        chart[(F + off) % 12].append(star)
    return chart


def 명궁_주성(국수: int, 음력생일: int, 명궁_idx: int) -> list:
    return 자미_14주성(국수, 음력생일).get(명궁_idx, [])


# --- 명리 원국: 형충회합·공망 진단 → wonmyung 키 ---------------------
_충 = [("子", "午"), ("丑", "未"), ("寅", "申"), ("卯", "酉"), ("辰", "戌"), ("巳", "亥")]
_육합 = [("子", "丑"), ("寅", "亥"), ("卯", "戌"), ("辰", "酉"), ("巳", "申"), ("午", "未")]
_방합 = [("寅", "卯", "辰"), ("巳", "午", "未"), ("申", "酉", "戌"), ("亥", "子", "丑")]
_자형 = ["辰", "午", "酉", "亥"]


def 원국_형충공망(지지목록, 공망지지) -> list:
    """4지지[년월일시] + 공망지지(set) → [(조건명, wonmyung_idx)]. wonmyung 대10/17/18/19/23/24."""
    s = set(지지목록)
    found = []
    # 충(대10)
    for i, (a, b) in enumerate(_충, 1):
        if a in s and b in s:
            found.append((f"{a}{b} 충(沖)", f"10-{i}"))
    # 육합(대24)
    for i, (a, b) in enumerate(_육합, 1):
        if a in s and b in s:
            found.append((f"{a}{b} 육합(六合)", f"24-{i}-A"))
    # 방합(대23)
    for i, trio in enumerate(_방합, 1):
        if all(x in s for x in trio):
            found.append((f"{''.join(trio)} 방합(方合)", f"23-{i}"))
    # 자형(대18)
    for i, z in enumerate(_자형, 1):
        if 지지목록.count(z) >= 2:
            found.append((f"{z}{z} 자형(自刑)", f"18-{i}-A"))
    # 삼형(대17)
    if {"寅", "巳", "申"} <= s:
        found.append(("寅巳申 삼형(三刑)", "17-1"))
    if {"丑", "戌", "未"} <= s:
        found.append(("丑戌未 삼형(三刑)", "17-5"))
    if {"子", "卯"} <= s:
        found.append(("子卯 형(刑)", "17-9"))
    # 공망(대19): 년/월/일/시지가 공망지지에 들면
    위치 = ["년지", "월지", "일지", "시지"]
    gm = []
    for idx, z in enumerate(지지목록):
        if z in 공망지지:
            gm.append(idx)
            found.append((f"{위치[idx]} 공망(空亡)", f"19-{idx + 1}"))
    return found


